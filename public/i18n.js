// Current language
window.currentLang = localStorage.getItem("mulle_lang") || "fa";

// Get translation
window.t = key => {
  const l = window.LANGS[window.currentLang];
  return l?.[key] ?? key;
};

// Switch language
window.switchLang = lang => {
  window.currentLang = lang;
  localStorage.setItem("mulle_lang", lang);
  location.reload();
};
